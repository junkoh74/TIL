package model.vo;

public class ProductVO {
	private int apple;
	private int banana;
	private int hala;
	
	
	public int getApple() {
		return apple;
	}
	public void setApple(int apple) {
		this.apple += apple;
	}
	public int getBanana() {
		return banana;
	}
	public void setBanana(int banana) {
		this.banana += banana;
	}
	public int getHala() {
		return hala;
	}
	public void setHala(int hala) {
		this.hala += hala;
	}
	
	
}
